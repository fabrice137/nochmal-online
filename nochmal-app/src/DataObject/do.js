function fillLetterScores(){
  let scoreArray =  [5,3,3,3,2,2,2,1,2,2,2,3,3,3,5];
  let rowLetters =  "A,B,C,D,E,F,G,H,I,J,K,L,M,N,O".split(",");
  let initLetterScore = [];

  for(let i=0; i<scoreArray.length; i++){
    let v = scoreArray[i];
    initLetterScore.push(
      {pos:"columnScore",
        txt: v,
        ltt: rowLetters[i],
        sta: 0
      });
  }
  return initLetterScore;
}

function fillColorScores (){
  let b = 0, g = 0, o = 0, p = 0, y = 0;

  for(let i=0; i<starterTable.length; i++){
    for(let j=0; j<starterTable[i].length; j++){
      
      if(starterTable[i][j].clr === "b") b++;
      else if(starterTable[i][j].clr === "g") g++;
      else if(starterTable[i][j].clr === "o") o++;
      else if(starterTable[i][j].clr === "p") p++;
      else if(starterTable[i][j].clr === "y") y++;
    }
  }
  return [
    {name:"green", count: g, score: 5}, 
    {name:"yellow", count: y, score: 5}, 
    {name:"blue", count: b, score: 5}, 
    {name:"pink", count: p, score: 5}, 
    {name:"orange", count: o, score: 5}
  ];
}



